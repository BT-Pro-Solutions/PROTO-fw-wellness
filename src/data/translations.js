export function t(key, lang, vars = {}) {
  const entry = translations[key]
  if (!entry) return key
  let text = entry[lang] || entry.en || key
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, v)
  })
  return text
}
